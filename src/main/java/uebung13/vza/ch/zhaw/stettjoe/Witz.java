package uebung13.vza.ch.zhaw.stettjoe;


public class Witz {
	private String type;
	private String category;
	
	
	public Witz (String type, String category) {
		this.type = type;
		this.category = category;
	}
	
	public String getType() {
		return type;
	}
	
	public String getCategory() {
		return category;
	}
}
